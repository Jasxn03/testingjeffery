def plot_right_sv_vs_r(aspect_ratios):
    """
    Show how the dominant right singular vector (most dangerous A mode)
    changes with aspect ratio. Plotted as a heatmap: rows = r values,
    columns = A_ij components.
    """
    comp_labels = [f'$A_{{{i+1}{j+1}}}$'
                   for i in range(3) for j in range(3)]
    v1_matrix = []

    for r in aspect_ratios:
        axes  = [float(r), 1.0, 1.0]
        x_tip = np.array([float(r), 0.0, 0.0])
        M, _  = build_transfer_matrix(axes, x_tip, mu=MU)
        M_norm = M /float(r)
        _, sv, Vt = svd(M_norm, full_matrices=False)
        # Sign convention: make largest-magnitude component positive
        v1 = Vt[0]
        if v1[np.argmax(np.abs(v1))] < 0:
            v1 = -v1
