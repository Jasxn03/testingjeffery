def plot_cov_comparison(name, M, cov_A, tau_empirical, tau_linear):
    """
    For one surface point, compare:
      - Analytical Cov(tau) = M Cov(A) M^T  (linear prediction, lab-frame A)
      - Empirical Cov(tau) from body-frame time series  (full nonlinear)
      - Empirical Cov(tau) from linear prediction time series

    Also plot variance of ||tau|| for all three.
    """
    cov_analytical = analytical_traction_cov(M, cov_A)
    cov_empirical  = np.cov(tau_empirical.T)
    cov_linear     = np.cov(tau_linear.T)

    fig, axes = plt.subplots(1, 3, figsize=(15, 5))

    vmax = max(np.abs(cov_analytical).max(),
               np.abs(cov_empirical).max(),
               np.abs(cov_linear).max())

    titles = [
        'Analytical\n$M\\,\\mathrm{Cov}(A)\\,M^T$ (lab-frame A, no rotation)',
        'Empirical\n(body-frame A, full orientation coupling)',
        'Linear prediction\n($M\\,\\mathrm{vec}(A_{\\mathrm{lab}})$, no body frame)',
    ]
    mats = [cov_analytical, cov_empirical, cov_linear]

    for ax, mat, title in zip(axes, mats, titles):
        im = ax.imshow(mat, cmap='RdBu_r', vmin=-vmax, vmax=vmax)
        ax.set_xticks([0, 1, 2])
        ax.set_yticks([0, 1, 2])
        ax.set_xticklabels(['$\\tau_x$', '$\\tau_y$', '$\\tau_z$'])
        ax.set_yticklabels(['$\\tau_x$', '$\\tau_y$', '$\\tau_z$'])
        for ii in range(3):
            for jj in range(3):
                ax.text(jj, ii, f'{mat[ii, jj]:.3f}',
                        ha='center', va='center', fontsize=9,
                        color='black' if abs(mat[ii, jj]) < 0.6*vmax
                        else 'white')
        plt.colorbar(im, ax=ax, shrink=0.8)
        ax.set_title(title, fontsize=10, fontweight='bold')

    # Compute relative error between analytical and empirical
    diag_anal = np.diag(cov_analytical)
    diag_emp  = np.diag(cov_empirical)
    rel_err   = np.abs(diag_anal - diag_emp) / (np.abs(diag_emp) + 1e-30)

    fig.suptitle(
        f'Traction covariance: analytical vs empirical\n'
        f'Point: {name}\n'
        f'Relative error on diagonal (orientation coupling effect): '
        f'{rel_err[0]:.3f}, {rel_err[1]:.3f}, {rel_err[2]:.3f}',
        fontsize=11, fontweight='bold'
    )
    fig.tight_layout()
    safe_name = name.replace('$', '').replace('/', '_').replace(
        ' ', '_').replace('\\', '').replace(',', '')[:30]
    fname = OUTDIR + f'step11b_cov_{safe_name}.png'
    fig.savefig(fname, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved {fname}")

    return {
        'cov_analytical': cov_analytical,
        'cov_empirical':  cov_empirical,
        'cov_linear':     cov_linear,
        'rel_err_diag':   rel_err,
    }


