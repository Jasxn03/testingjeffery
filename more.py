def surface_points_body_frame(a):
    """
    Return a dict of named surface points in the body frame.
    These are the natural high-stress candidates by symmetry.

    Points included:
      tip_pos  : (a, 0, 0)   — positive tip of major axis
      tip_neg  : (-a, 0, 0)  — negative tip
      eq_b_pos : (0, b, 0)   — equator along b-axis
      eq_b_neg : (0, -b, 0)
      eq_c_pos : (0, 0, c)   — equator along c-axis
      eq_c_neg : (0, 0, -c)
      off_45   : point on surface at 45 degrees between a and b axes
    """
    a0, b0, c0 = a[0], a[1], a[2]

    # 45-degree point between a and b axes: solve x^2/a^2 + y^2/b^2 = 1
    # with x = y*a/b  =>  x = a/sqrt(2), y = b/sqrt(2)
    x45 = a0 / np.sqrt(2)
    y45 = b0 / np.sqrt(2)

    return {
        'tip $(a,0,0)$':       np.array([a0,  0.,  0.]),
        'tip $(-a,0,0)$':      np.array([-a0, 0.,  0.]),
        'equator $(0,b,0)$':   np.array([0.,  b0,  0.]),
        'equator $(0,0,c)$':   np.array([0.,  0.,  c0]),
        '45° $(a/\\sqrt{2}, b/\\sqrt{2}, 0)$':
                               np.array([x45, y45, 0.]),
    }


def outward_normal(x, a):
    """
    Outward unit normal to ellipsoid surface at point x.
    n_i = x_i / a_i^2,  normalised.
    """
    n = x / (a**2)
    return n / np.linalg.norm(n)


def traction_transfer_matrix(ell, x, a):
    """
    Compute the 3x9 traction transfer matrix M at surface point x.

    tau(x) = M @ vec(A)

    where vec(A) is the row-major flattening of the 3x3 velocity
    gradient tensor A, and tau is the tangential traction vector.

    Strategy: linearity of Stokes flow means we can probe each of the
    9 basis directions of A independently, compute the resulting traction,
    and assemble M column by column.

    Parameters
    ----------
    ell : Ellipsoid  (already in surface mode)
    x   : (3,) surface point in body frame
    a   : (3,) semi-axes

    Returns
    -------
    M : (3, 9) transfer matrix
    """
    x   = np.asarray(x, dtype=float)
    n   = outward_normal(x, a)
    M   = np.zeros((3, 9))

    for k in range(9):
        # Build basis A_k: 1 in position (k//3, k%3), 0 elsewhere
        A_basis        = np.zeros((3, 3))
        A_basis[k // 3, k % 3] = 1.0

        ell.set_strain(A_basis)
        ell.set_coefs()

        sig   = ell.sigma(x)          # (3, 3) stress tensor
        t_vec = sig @ n               # traction vector
        tau   = t_vec - np.dot(t_vec, n) * n   # tangential component

        M[:, k] = tau

    return M


def vec(A):
    """Row-major vectorisation of a 3x3 matrix. Shape (9,)."""
    return A.flatten()


