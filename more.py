def compute_transfer_matrices(axes):
    """
    For each named surface point, compute M and its SVD.

    Returns dict: point_name -> {
        'M'      : (3, 9) transfer matrix,
        'U'      : (3, 3) left singular vectors,
        'sv'     : (3,)   singular values,
        'Vt'     : (9, 9) right singular vectors (rows),
        'x'      : (3,)   surface point,
        'n'      : (3,)   outward normal,
    }
    """
    a    = np.array(axes, dtype=float)
    eps0 = np.zeros((3, 3))
    ell  = Ellipsoid(a, eps0, mu=MU)
    ell.use_surface_mode()

    points  = surface_points_body_frame(a)
    results = {}

    for name, x in points.items():
        print(f"  Computing M at {name}...", end=" ", flush=True)
        M = traction_transfer_matrix(ell, x, a)

        # SVD: M = U @ diag(sv) @ Vt
        U, sv, Vt = svd(M, full_matrices=True)

        results[name] = {
            'M':  M,
            'U':  U,
            'sv': sv,
            'Vt': Vt,
            'x':  x,
            'n':  outward_normal(x, a),
        }
        print(f"singular values = {sv}")

    return results


# ─────────────────────────────────────────────────────────────
# STEP 11b — ANALYTICAL vs EMPIRICAL TRACTION COVARIANCE
# ─────────────────────────────────────────────────────────────

def compute_cov_A(A_series):
    """
    Empirical covariance of vec(A) from the time series.
    Returns (9, 9) matrix.
    """
    vecs = A_series.reshape(-1, 9)           # (T, 9)
    return np.cov(vecs.T)                    # (9, 9)


def analytical_traction_cov(M, cov_A):
    """
    Predicted traction covariance from linearity:
    Cov(tau) = M @ Cov(vec(A)) @ M.T   shape (3, 3)
    """
    return M @ cov_A @ M.T


def empirical_traction_series(A_series, R_history, axes, mu, x_body):
    """
    Compute traction at body-frame point x_body at every timestep,
    transforming A into the body frame each time.
    Full nonlinear result including orientation coupling.
    Uses fast_traction_vectors for speed — vectorised einsum over all T.
    """
    M, n_hat = build_transfer_matrix(axes, x_body, mu=mu)
    return fast_traction_vectors(M, n_hat, A_series, R_history)  # (T, 3)


def linear_traction_series(A_series, M):
    """
    Traction predicted by the LINEAR transfer matrix (no orientation coupling):
    tau_linear(t) = M @ vec(A_lab(t))

    This treats A as if the particle were always axis-aligned — it ignores
    the body-frame rotation entirely. The difference between this and the
    empirical series quantifies the contribution of orientation coupling.
    """
    vecs = A_series.reshape(-1, 9)    # (T, 9)
    return (M @ vecs.T).T             # (T, 3)


