# STEP 11c — AMPLIFICATION vs ASPECT RATIO
# ─────────────────────────────────────────────────────────────

def amplification_vs_aspect_ratio(aspect_ratios):
    sigma1_list = []
    sigma2_list = []
    sigma3_list = []

    for r in aspect_ratios:
        axes  = [float(r), 1.0, 1.0]
        x_tip = np.array([float(r), 0.0, 0.0])
        print(f"  r={r:.2f}  tip={x_tip}...", end=" ", flush=True)

        M, _ = build_transfer_matrix(axes, x_tip, mu=MU)
        M_norm = M /float(r)
        _, sv, _ = svd(M_norm, full_matrices=False)

        sigma1_list.append(sv[0])
        sigma2_list.append(sv[1] if len(sv) > 1 else 0.0)
        sigma3_list.append(sv[2] if len(sv) > 2 else 0.0)
        print(f"sv = {sv}")

    return (np.array(aspect_ratios),
            np.array(sigma1_list),
            np.array(sigma2_list),
            np.array(sigma3_list))

