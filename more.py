"""
step11_transfer_matrix.py

Transfer matrix analysis: how does the velocity gradient tensor A map
to surface traction on an ellipsoid?

Because Stokes flow is linear, the traction vector tau at any surface
point x is a linear function of vec(A):

    tau(x) = M(x, r) @ vec(A)       shape: (3,) = (3, 9) @ (9,)

where M is the 3x9 traction transfer matrix, x is the surface point
in the BODY FRAME, and r = a/b is the aspect ratio.

This file computes and analyses M across:
  - Multiple surface points (tip, equator, off-axis)
  - Multiple aspect ratios

Key outputs
-----------
1. SVD of M at each surface point and aspect ratio
     - Singular values: how strongly each mode of A is amplified
     - Right singular vectors: which components of A drive traction
     - Left singular vectors: which traction directions are excited
2. Analytical traction covariance from CM empirical Cov(A)
     Cov(tau) = M @ Cov(vec(A)) @ M.T
   compared against the empirical traction covariance from the time series
3. Dominant amplification (largest singular value) vs aspect ratio curve
4. Comparison of analytical vs empirical traction variance
   — breakdown of agreement quantifies the contribution of orientation
     coupling (the nonlinear effect rotation has on body-frame A)

Outputs saved to ra_coupling_plots/step11_*.png

Usage
-----
Place alongside jeffery4.py, orientation.py, grad_u.csv.
Run after grad_u.csv has been generated by grad_u_model.py.
"""

