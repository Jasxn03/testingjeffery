def plot_traction_timeseries_comparison(times, tau_empirical,
                                        tau_linear, name):
    """
    Time series comparison of ||tau|| from empirical (full coupling)
    vs linear (M @ vec(A_lab)) prediction.
    Shows where and when orientation coupling matters most.
    """
    mag_emp = np.linalg.norm(tau_empirical, axis=1)
    mag_lin = np.linalg.norm(tau_linear,    axis=1)
    diff    = mag_emp - mag_lin

    fig, axes = plt.subplots(3, 1, figsize=(14, 9), sharex=True)

    axes[0].plot(times, mag_emp, lw=0.8, color='#E07B39',
                 label='Empirical (body-frame, full coupling)')
    axes[0].plot(times, mag_lin, lw=0.8, color='#4472C4',
                 alpha=0.7, label='Linear ($M\\,\\mathrm{vec}(A_{lab})$)')
    axes[0].set_ylabel('$\\|\\tau\\|$', fontsize=11)
    axes[0].set_title(f'Traction magnitude: point {name}',
                      fontsize=11, fontweight='bold')
    axes[0].legend(fontsize=9)
    axes[0].grid(True, alpha=0.3)

    axes[1].plot(times, diff, lw=0.8, color='crimson', alpha=0.8)
    axes[1].axhline(0, color='grey', lw=0.8)
    axes[1].set_ylabel('Empirical $-$ Linear', fontsize=11)
    axes[1].set_title('Difference (orientation coupling contribution)',
                      fontsize=11, fontweight='bold')
    axes[1].grid(True, alpha=0.3)

    # Rolling std of difference as a proxy for coupling strength over time
    window = max(1, len(diff) // 100)
    roll_std = pd.Series(diff).rolling(window, center=True).std().values
    axes[2].plot(times, roll_std, lw=1.0, color='purple')
    axes[2].set_ylabel('Rolling std of difference', fontsize=11)
    axes[2].set_xlabel('Time ($\\tau_\\eta$)', fontsize=11)
    axes[2].set_title(f'Local coupling strength '
                      f'(rolling window = {window} steps)',
                      fontsize=11, fontweight='bold')
    axes[2].grid(True, alpha=0.3)

    fig.suptitle(
        f'Linear vs full traction: point {name}\n'
        f'Pearson r = {np.corrcoef(mag_emp, mag_lin)[0,1]:.4f}   '
        f'Mean |diff| = {np.mean(np.abs(diff)):.4f}   '
        f'RMS diff = {np.sqrt(np.mean(diff**2)):.4f}',
        fontsize=11, fontweight='bold'
    )
    fig.tight_layout()
    safe_name = name.replace('$', '').replace('/', '_').replace(
        ' ', '_').replace('\\', '').replace(',', '')[:30]
    fname = OUTDIR + f'step11b_timeseries_{safe_name}.png'
    fig.savefig(fname, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved {fname}")


